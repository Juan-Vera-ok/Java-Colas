public class Usuario
{
   private String nombre;
   private int prioridad;

   public void setNombre(String a)
   {
      nombre=a;
   }
   public String getNombre()
   {
      return nombre;
   }
   public void setPrioridad(int b)
   {
      prioridad=b;
   }
   public int getPrioridad()
   {
      return prioridad;
   }
   
   
}